import java.util.*;
public class Solution {
    public static void main(String[] args) {
       Scanner scan = new Scanner(System.in);
       int n = scan.nextInt();
       for (int i=1;i<=n;i++){

          System.out.print(i+" ");
       }
        for (int i=n;i>0;i--){

          System.out.print(i+" ");
       } 
       
    }
}
